    for domain in DOMAINS:
        for subdomain in domain['subdomains']:
            subdomain_count += 1
            for level in range(1, 6):
                if level not in subdomain['levels']:
                    raise ValueError(f"Missing level {level} in {subdomain['name']}")
                cells = subdomain['levels'][level]
                for dim in DIMENSIONS:
                    total_cells += 1
